#include <yiot-iotkit/root-of-trust/KSQRoT.h>

//-----------------------------------------------------------------------------
KSQRoT::KSQRoT(const QString &id, const QString &name, const QString &image) : QObject() {
    qDebug() << "New Root of trust: " << id << "  :  " << name;
    m_id = id;
    m_name = name;
    m_image = image;
}

//-----------------------------------------------------------------------------
KSQRoT::KSQRoT(const KSQRoT &r) {
}

//-----------------------------------------------------------------------------
QString
KSQRoT::id() const {
    return m_id;
}

//-----------------------------------------------------------------------------
QString
KSQRoT::name() const {
    return m_name;
}

//-----------------------------------------------------------------------------
QString
KSQRoT::image() const {
    return m_image;
}

